# WAP to find the length of a string.
s1 = input("Enter a string: ")
print("Length of string:", len(s1))


# WAP to convert the lower-case characters of a string into upper case.
s2 = input("Enter a string: ")
print("Uppercase of string:", s2.upper())
print("Uppercase of string:", s2.lower())

# WAP to append/concatenate a string to another string.
s3 = input("Enter String 1: ")
s4 = input("Enter String 2: ")
s5 = s3 + s4
print("\nAppended String:", s5)

#WAP to reverse a given string.
s6 = input("Enter a string: ")
print("Reverse of string:", s6[::-1])


